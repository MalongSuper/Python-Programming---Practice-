# Given Formula : MPG = (Miles driven) / (Gallons of gas used)
# Input
print("Calculating a car's miles-per-gallon (MPG)")
Miles = (float(input("Enter the number of miles driven: ")))
print(Miles, "of miles driven")
Gallons = (float(input("Enter the gallons of gas used: ")))
print(Gallons, "of gas used")
# Calculate using the formula
MPG = Miles / Gallons
# Display Results
print("MPG =", MPG)
